-module(poke_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(normal, []) ->
	Dispatch = cowboy_router:compile([
		{'_', [
			{"/websocket", poke_ws, []}
		]}
	]),
	{ok, _} = cowboy:start_clear(http, [{port, 80}], #{
		env => #{dispatch => Dispatch}
	}),
    poke_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

